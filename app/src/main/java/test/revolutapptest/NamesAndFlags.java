public class NamesAndFlags {
    private String fullName;
    private String flag;

    public NamesAndFlags(String fullName, String flag) {
        this.fullName = fullName;
        this.flag = flag;
    }

    public String getFullName() {
        return fullName;
    }

    public String getFlag() {
        return flag;
    }
}
